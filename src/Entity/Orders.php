<?php

namespace App\Entity;

use App\Repository\OrdersRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: OrdersRepository::class)]
class Orders
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 20, unique: true)]
    private ?string $reference = null;

    #[ORM\Column]
    private ?\DateTimeImmutable $created_at = null;

    #[ORM\ManyToOne(inversedBy: 'orders')]
    private ?Coupons $coupons = null;

    #[ORM\ManyToOne(inversedBy: 'orders')]
    #[ORM\JoinColumn(nullable: false)]
    private ?Users $users = null;

    #[ORM\OneToMany(mappedBy: 'orders', targetEntity: OrdesDetails::class)]
    private Collection $ordesDetails;

    public function __construct()
    {
        $this->ordesDetails = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getReference(): ?string
    {
        return $this->reference;
    }

    public function setReference(string $reference): self
    {
        $this->reference = $reference;

        return $this;
    }

    public function getCreatedAt(): ?\DateTimeImmutable
    {
        return $this->created_at;
    }

    public function setCreatedAt(\DateTimeImmutable $created_at): self
    {
        $this->created_at = $created_at;

        return $this;
    }

    public function getCoupons(): ?Coupons
    {
        return $this->coupons;
    }

    public function setCoupons(?Coupons $coupons): self
    {
        $this->coupons = $coupons;

        return $this;
    }

    public function getUsers(): ?Users
    {
        return $this->users;
    }

    public function setUsers(?Users $users): self
    {
        $this->users = $users;

        return $this;
    }

    /**
     * @return Collection<int, OrdesDetails>
     */
    public function getOrdesDetails(): Collection
    {
        return $this->ordesDetails;
    }

    public function addOrdesDetail(OrdesDetails $ordesDetail): self
    {
        if (!$this->ordesDetails->contains($ordesDetail)) {
            $this->ordesDetails->add($ordesDetail);
            $ordesDetail->setOrders($this);
        }

        return $this;
    }

    public function removeOrdesDetail(OrdesDetails $ordesDetail): self
    {
        if ($this->ordesDetails->removeElement($ordesDetail)) {
            // set the owning side to null (unless already changed)
            if ($ordesDetail->getOrders() === $this) {
                $ordesDetail->setOrders(null);
            }
        }

        return $this;
    }
}
